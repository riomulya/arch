// Helper functions
export const getAllProducts = () => {
  return productCategories.flatMap(category => category.products);
};

export const getProductBySlug = (slug) => {
  return getAllProducts().find(product => product.slug === slug);
};

export const getProductsByCategory = (categoryId) => {
  const category = productCategories.find(cat => cat.id === categoryId);
  return category ? category.products : [];
};

export const getRelatedProducts = (currentProduct, limit = 3) => {
  return getAllProducts()
    .filter(product => product.category === currentProduct.category && product.id !== currentProduct.id)
    .slice(0, limit);
};

export const searchProducts = (query) => {
  const searchTerm = query.toLowerCase();
  return getAllProducts().filter(product =>
    product.name.toLowerCase().includes(searchTerm) ||
    product.category.toLowerCase().includes(searchTerm) ||
    product.subcategory.toLowerCase().includes(searchTerm) ||
    product.description.toLowerCase().includes(searchTerm) ||
    product.applications.some(app => app.toLowerCase().includes(searchTerm))
  );
};

export default productCategories;